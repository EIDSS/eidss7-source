              AND b.idfsSite = ar.GrantingActorSiteID;

        INSERT INTO @FinalResults
        SELECT ID,
               BatchStatusTypeID,
               CASE WHEN EXISTS (SELECT ReadPermissionIndicator FROM @Results WHERE ID = ID) THEN 1 ELSE 0 END, 
